package Hmwk3;

public class Calc {





    public int sum (int x, int y){
        return x + y;
    }

    public int sub (int x, int y){
        return x - y;
    }

    public int multiple(int x, int y){
        return x * y;
    }

    public int div (int x, int y){
        return x / y;
    }
}





